    private void loadtable(){
        DefaultTableModel dtm = (DefaultTableModel) this.tblSV.getModel();
            dtm.setRowCount(0);
        for (SinhVien sv : this.svRepo.fillAll()) {
            Object[] row = {
                sv.getMaSV(),
                sv.getHoTen(),
                sv.getEmail(),                
                sv.getSĐT(),
                sv.getGioiTinh() == 1 ? "Nam" : "Nữ",
                sv.getDiaChi(),
            };
            dtm.addRow(row);
        }
    }    
