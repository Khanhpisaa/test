    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String ma = this.txtMaSV.getText();
        String ten = this.txtHoTen.getText();
        String SDT = this.txtSĐT.getText();
        String Email = this.txtEmail.getText();
        String diachi = this.txtDiaChi.getText();
        int gioiTinh = this.rdoNam.isSelected() ? 1 : 0;
        SinhVien sv = new SinhVien(ma, ten, Email, gioiTinh, SDT, diachi);
       this.svRepo.update(sv);
       this.loadtable();
        JOptionPane.showMessageDialog(this, "Updated Successfully");
    }//GEN-LAST:event_btnUpdateActionPerformed

